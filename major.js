function getScore(nounsOne, nounsTwo) {
    if(nounsOne.length == 0 && nounsTwo.length == 0) return 0
    let totalScore = 0
    nounsOne.forEach(noun => {
        totalScore += noun.score
    })
    nounsTwo.forEach(noun => {
        totalScore += noun.score
    })
    let totalNouns = nounsOne.length + nounsTwo.length
    return totalScore / totalNouns
}

